public class FunctionValue {

    public double x;
    public double y;

    public FunctionValue(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "FunctionValue{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }

}
